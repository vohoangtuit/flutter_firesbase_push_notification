final Map<String, Item> _items = <String, Item>{};
Item _itemForMessage(Map<String, dynamic> message) {
  final dynamic data = message['data'] ?? message;
  final String itemId = data['description'];
  final Item item = _items.putIfAbsent(itemId, () => Item(description: itemId))
    ..type = data['type']
    ..content = data['content'];
  return item;
}

class Item {
  Item({this.description});
  final String description;

  StreamController<Item> _controller = StreamController<Item>.broadcast();
  Stream<Item> get onChanged => _controller.stream;

  String type;
  String get getType => type;
  set getType(String value) {
    type = value;
    _controller.add(this);
  }

  String content;
  String get getContent => content;
  set getContent(String value) {
    content = value;
    _controller.add(this);
  }

  static final Map<String, Route<void>> routes = <String, Route<void>>{};
  Route<void> get route {
    final String routeName = '/detail/$description';
    return routes.putIfAbsent(
      routeName,
          () => MaterialPageRoute<void>(
        settings: RouteSettings(name: routeName),
        builder: (BuildContext context) => DetailPage(description),
      ),
    );
  }
}
